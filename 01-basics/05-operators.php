<?php
$a = 10;
$b = 20;

// Arithmetic Operators
echo $a + $b;
echo $a - $b;
echo $a * $b;
echo $a / $b;
echo $a % $b;

// Comparison Operators
var_dump($a == $b);
var_dump($a != $b);
var_dump($a > $b);
var_dump($a < $b);

// Logical Operators
var_dump($a && $b);
var_dump($a || $b);
var_dump(!$a);
?>